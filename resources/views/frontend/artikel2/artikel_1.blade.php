            <p>Menurut Ketua Dewan Pengurus AAJI, Budi Tampubolon dari total penduduk di Indonesia, penetrasi asuransi generasi milenial lebih rendah dibandingkan segmen usia yang lebih tua. "Sebagian besar dari mereka paham produk perbankan dan asuransi. Jumlahnya mencapai 67 persen. Tapi yang memiliki asuransi sekitar 6-7 persen," ujar Budi.</p>
            <p>Berangkat dari fakta tersebut, sebenarnya asuransi semestinya dilihat dari sudut pandang lain. Di masa pandemi seperti sekarang ini, asuransi sangat membantu dalam meningkatkan kualitas hidup masyarakat.</p>
            <p>Namun sayangnya, sebagian orang berpikir bahwa asuransi tidak perlu karena melihat kondisi fisik saat ini. Sebelum bahas lebih jauh tentang manfaat asuransi, yuk sadari bersama 5 kesalahan atau anggapan orang bahwa asuransi itu nggak penting:</p>

            <p class="bold">1. Beli Asuransi Kesehatan Sama Dengan Buang Uang</p>
            <p>Ini adalah anggapan yang benar-benar keliru! Anggapan ini muncul karena Anda mungkin belum menyadari manfaat asuransi untuk kehidupan. Kemungkinan lain, mungkin Anda salah membeli produk asuransi.</p>
            <p>Ketika Anda menjadi nasabah asuransi, artinya uang yang dikeluarkan bukan menjadi tabungan melainkan masuk ke dalam kategori pengeluaran, dengan tujuan perencanaan keuangan primer serta bermanfaat di kemudian hari.</p>
       
            <p class="bold">2. Masih Muda dan Sehat Belum Perlu Asuransi</p>
            <p>Anggapan ini keliru karena sebenarnya asuransi perlu dimiliki di usia muda sedini mungkin dan dalam kondisi sehat. Tujuannya ketika menjadi nasabah asuransi, artinya Anda mengelola risiko-risiko kerugian finansial yang mungkin terjadi akibat kejadian-kejadian tertentu.</p>
            
            <p class="bold">3. Biayanya Mahal dan Sudah ada BPJS</p>
            <p>Masyarakat Indonesia memang diwajibkan untuk memiliki BJPS, apalagi di tengah kondisi pandemi seperti sekarang ini. Sekarang banyak pmeberitaan yang mengabarkan bahwa pasien Covid-19 ditanggung biaya perawatannya oleh Pemerintah. Namun ini biaya pertanggungan ini hanya berlaku di rumah sakit rujukan saja, tidak di rumah sakit swasta. </p>
            <p>Itu adalah salah satu kondisi nyata yang terjadi hari ini. Namun jika Anda ingin mendapatkan perlindungan maksimal, sangat dianjurkan untuk memiliki asuransi. Menyoal biaya atau premi, Anda pun dapat memilih premi sesuai dengan kebutuhan dan kemampuan.</p>
       
            <p class="bold">4. Percuma Beli Kalau Klaim Sulit</p>
            <p>Apakah Anda pernah mengalami klaim asuransi diajukan namun justru mendapat penolakan dari perusahaan asuransi? Jika ya, coba cek dulu alasan penolakan tersebut ya! Bisa jadi klaim tidak cair karena beberapa hal. Mulai dari dokumen tidak lengkap, tak sesuai dengan persyaratan polis atau diluar cakupan perlindungan.</p>

            <p class="bold">5. Hanya Dapat Manfaat Kalau Sakit</p>
            <p>Sebelum membeli produk asuransi, pastikan dulu Anda mengetahui beragam manfaat yang diberikan. Setiap produk asuransi menawarkan manfaat perlindungan yang berbeda-beda lho! Nah mungkin sekarang pertanyaanya, kapan harus membeli produk asuransi? Pastinya ketika Anda sudah memiliki penghasilan sendiri.</p>
            <p>Selagi berada di usia produktif dan bagi Anda yang sehat meski usia tak lagi muda, ada baiknya mulai membeli produk asuransi untuk perlindungan di kemudian hari. Nah dalam rangka Hari Pelanggan Nasional yang dirayakan setiap 4 September dan Hari Asuransi Nasional pada tanggal 18 Oktober, Allianz Indonesia siap memberikan perlindungan dari rumah setiap saat lewat Program eAZy di Rumah Aja.</p>
